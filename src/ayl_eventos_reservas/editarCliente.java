    private void botonEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonEliminarMouseClicked
        int filaSeleccionada = this.tablaClientes.getSelectedRow();
        if (filaSeleccionada != -1){
            int ax = JOptionPane.showConfirmDialog(null, "¿Seguro que desea borrar este cliente?");
            if(ax == JOptionPane.YES_OPTION){
                int id = Integer.valueOf((String)this.tablaClientes.getValueAt(filaSeleccionada, 0));
        
                gestor.eliminarCliente(id);
                setearTabla();
                personalizarTabla(tablaClientes);
                JOptionPane.showMessageDialog(null, "Se ha borrado el cliente");
            }
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún cliente a eliminar.\nSeleccione un cliente para eliminarlo.");
        }
    }//GEN-LAST:event_botonEliminarMouseClicked

