    private void botonGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonGuardarMouseClicked
        if("".equals(this.txtNombre.getText())){
            JOptionPane.showMessageDialog(null, "Complete los campos", "Completar", JOptionPane.WARNING_MESSAGE);
        }else{
            String nombre = txtNombre.getText();
            String mail = txtMail.getText();
            String tel = txtTelefono.getText();
            if(gestor.editarCliente(id, nombre, mail, tel)){
                JOptionPane.showMessageDialog(null, "El cliente se ha editado exitosamente");
                setearTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Hubo un error al editar el cliente.\nPor favor intente nuevamente.", "Error", 0);
            }
        }
    }//GEN-LAST:event_botonGuardarMouseClicked

