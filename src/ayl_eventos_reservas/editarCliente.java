    private void tablaClientesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMousePressed
        int filaSeleccionada = this.tablaClientes.getSelectedRow();
        if (filaSeleccionada != -1){
            this.id = Integer.valueOf((String)this.tablaClientes.getValueAt(filaSeleccionada, 0));
            this.txtNombre.setText((String)tablaClientes.getValueAt(filaSeleccionada, 1));
            this.txtMail.setText((String)tablaClientes.getValueAt(filaSeleccionada, 2));
            this.txtTelefono.setText((String)tablaClientes.getValueAt(filaSeleccionada, 3));
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún cliente.\nSeleccione un cliente para editar.");
        }
    }//GEN-LAST:event_tablaClientesMousePressed

